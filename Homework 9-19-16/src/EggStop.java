
public class EggStop {

	public static void top() {
		System.out.println("   _______");
		System.out.println("  /       \\");
		System.out.println(" /         \\");
	}
	
	public static void butt() {
		System.out.println(" \\         /");
		System.out.println("  \\_______/");
		
	}

	
	public static void main(String[] args) {
		top();
		butt();
		butt();
		System.out.println(" +---------+");
		top();
		System.out.println(" |   Stop  |");
		butt();
		top();
		System.out.println(" +---------+");
		
		
	}

}
